    public  void setAfterAtk2(int damage){
         if(select2.getATK() > select1.getDef()){
                select1.setHp(select1.getHp() - damage);
                if(select1.getHp() <= 0){
                    select1.setHp(0);
                    if(player1 < 6)
                    jComboBox1.setSelectedIndex(player1++);
                    else System.exit(0);
                }
            }else select1.setHp(select1.getHp() - 1);
     }
