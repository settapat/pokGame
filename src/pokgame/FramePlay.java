    public  int  getDm2(int skill){
        int damage = 0,spDef = 0,spAtk,power,level;
            
            spAtk = select2.getATK();
            power = select2.getLinklist().get(skill).getPow();
            level = select2.getLevel();
            
            spDef = select1.getSpDef();
            
            damage = (int)(((((double)2*level/5)+2)*power*(spAtk/spDef))/50)+2;
            return  damage;
    }
